namespace DPA_Musicsheets.Commands
{
    public static class CKey
    {
        public static string InsertClef     { get { return "LeftAlt+C"; } }
        public static string InsertTempo    { get { return "LeftAlt+S"; } }
        public static string InsertTime     { get { return "LeftAlt+T"; } }
        public static string InsertTime44   { get { return "LeftAlt+T+4"; } }
        public static string InsertTime34   { get { return "LeftAlt+T+3"; } }
        public static string InsertTime68   { get { return "LeftAlt+T+6"; } }

        public static string Open           { get { return "LeftCtrl+O"; } }
        public static string Save           { get { return "LeftCtrl+S"; } }
        public static string SavePdf        { get { return "LeftCtrl+S+P"; } }
    }
}
